const reverseDict = {};
for (const [en, vi] of Object.entries(dict)) {
    reverseDict[vi] = en;
}

// Override alert to translate dynamic alerts
const originalAlert = window.alert;
window.alert = function(msg) {
    const lang = localStorage.getItem('lang') || 'en';
    let displayMsg = msg;
    if (lang === 'vi' && dict[msg]) displayMsg = dict[msg];
    // If lang === 'en', msg is already in English, so we don't need to change it
    originalAlert(displayMsg);
};

function translateDOM(lang) {
    const isVi = lang === 'vi';
    const sourceDict = isVi ? dict : reverseDict;
    
    function walk(node) {
        if (node.nodeType === 3) {
            let text = node.nodeValue.trim();
            if (text && sourceDict[text]) {
                node.nodeValue = node.nodeValue.replace(text, sourceDict[text]);
            }
        } else if (node.nodeType === 1) {
            if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
                for (let child of node.childNodes) walk(child);
            }
            if (node.placeholder) {
                let text = node.placeholder.trim();
                if (sourceDict[text]) {
                    node.placeholder = sourceDict[text];
                }
            }
            if (node.value && (node.tagName === 'OPTION' || node.tagName === 'INPUT')) {
                let text = node.value.trim();
                if (sourceDict[text]) {
                    // Only translate option text, don't change value for forms if possible, 
                    // but since this is frontend only, we can translate inner text of options instead.
                }
            }
        }
    }
    
    walk(document.body);
}

// Observe dynamic DOM changes to translate them automatically
const observer = new MutationObserver((mutations) => {
    const lang = localStorage.getItem('lang') || 'en';
    if (lang !== 'vi') return; // Only translate dynamically if we are in VN mode (English is default)

    observer.disconnect(); // Prevent infinite loops during translation
    
    mutations.forEach(mutation => {
        if (mutation.type === 'childList') {
            mutation.addedNodes.forEach(node => {
                if (node.nodeType === 1 || node.nodeType === 3) {
                    const sourceDict = dict; // Always dict because default DOM is English
                    function walk(n) {
                        if (n.nodeType === 3) {
                            let text = n.nodeValue.trim();
                            if (text && sourceDict[text]) {
                                n.nodeValue = n.nodeValue.replace(text, sourceDict[text]);
                            }
                        } else if (n.nodeType === 1) {
                            if (n.tagName !== 'SCRIPT' && n.tagName !== 'STYLE') {
                                for (let child of n.childNodes) walk(child);
                            }
                            if (n.placeholder) {
                                let text = n.placeholder.trim();
                                if (sourceDict[text]) n.placeholder = sourceDict[text];
                            }
                            if (n.value && (n.tagName === 'OPTION' || n.tagName === 'INPUT')) {
                                let text = n.value.trim();
                                // Do nothing for value, it breaks form logic
                            }
                        }
                    }
                    walk(node);
                }
            });
        }
    });

    // Re-attach observer
    observer.observe(document.body, { childList: true, subtree: true });
});

function setLang(lang) {
    const currentLang = document.documentElement.lang || 'en';
    if (currentLang === lang) return; // Already in this lang
    
    translateDOM(lang);
    
    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);
    
    const enBtn = document.getElementById('lang-en');
    const viBtn = document.getElementById('lang-vi');
    if(enBtn && viBtn) {
        enBtn.classList.toggle('active', lang === 'en');
        viBtn.classList.toggle('active', lang === 'vi');
    }

    // Set up observer for dynamic changes
    if (lang === 'vi') {
        observer.observe(document.body, { childList: true, subtree: true });
    } else {
        observer.disconnect();
    }
    
    // Handle cart dynamic items
    if (typeof updateCartUI === 'function' && typeof cart !== 'undefined') {
        cart.forEach(item => {
            let baseName = item.name.split(' (')[0];
            let size = item.name.split(' (')[1] ? ' (' + item.name.split(' (')[1] : '';
            if (lang === 'vi' && dict[baseName]) {
                item.name = dict[baseName] + size;
            } else if (lang === 'en' && reverseDict[baseName]) {
                item.name = reverseDict[baseName] + size;
            }
        });
        updateCartUI(); // re-render
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('lang');
    if (saved && saved === 'vi') {
        setTimeout(() => setLang('vi'), 10);
    }
});